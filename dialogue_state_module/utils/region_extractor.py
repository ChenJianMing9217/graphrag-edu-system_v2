from typing import Optional

def extract_region(user_query: str) -> Optional[str]:
    """
    從用戶輸入中提取台灣縣市地區。
    支援 22 縣市全名及其簡稱。
    """
    regions = [
        "臺北市", "新北市", "桃園市", "臺中市", "臺南市", "高雄市",
        "基隆市", "新竹市", "嘉義市", "新竹縣", "苗栗縣", "彰化縣",
        "南投縣", "雲林縣", "嘉義縣", "屏東縣", "宜蘭縣", "花蓮縣",
        "臺東縣", "澎湖縣", "金門縣", "連江縣",
        "台北市", "台中市", "台南市", "台東縣",
        "臺北", "新北", "桃園", "臺中", "臺南", "高雄",
        "基隆", "新竹", "嘉義", "苗栗", "彰化", "南投",
        "雲林", "屏東", "宜蘭", "花蓮", "臺東", "澎湖",
        "金門", "馬祖",
        "台北", "台中", "台南", "台東"
    ]
    
    # 簡單的關鍵字匹配
    for r in regions:
        if r in user_query:
            # 正規化名稱
            res = r
            if r == "馬祖": res = "連江縣"
            
            # 統一將 "台" 轉為 "臺"
            res = res.replace("台", "臺")
            
            if res in ["臺北", "新北", "桃園", "臺中", "臺南", "高雄", "基隆", "新竹", "嘉義"]:
                return res + "市"
            if res in ["苗栗", "彰化", "南投", "雲林", "屏東", "宜蘭", "花蓮", "臺東", "澎湖", "金門"]:
                return res + "縣"
            return res
    return None
